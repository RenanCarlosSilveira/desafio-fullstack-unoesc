package br.edu.unoesc.desafiofullstackunoesc.domain;

public class Uf {
	public String nome;
	public String sigla;

	public Uf() {
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getSigla() {
		return sigla;
	}

	public void setSigla(String sigla) {
		this.sigla = sigla;
	}

	
}
